#include "libft.h"

char	**ft_strsplit(char const s, char c)
{
	int	i;
	int	j;
	
	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		while (s[i] != c)
			j++;
		return(ft_strsub(s, i, j));
	}

}
